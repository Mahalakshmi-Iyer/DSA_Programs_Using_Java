package Functions;

public class MathFuncs {
    public static void main(String[] args) {
        int a = 9284;
        int b = 2937;
        System.out.println("Minimum of " + a + " and " + b + " is " + Math.min(a, b));
        System.out.println("Maximum of " + a + " and " + b + " is " + Math.max(a, b));
        System.out.println("Square root of " + a + " is " + Math.sqrt(a));
        System.out.println("Square of " + a + " is " + Math.pow(a, 2));
        System.out.println("Absolute of -9273.91 is " + Math.abs(-9273.91));
    }
}
